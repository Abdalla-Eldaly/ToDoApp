// ignore_for_file: must_be_immutable

part of 'task_delete_bloc.dart';

/// Represents the state of TaskDelete in the application.
class TaskDeleteState extends Equatable {
  TaskDeleteState({
    this.rangeStart,
    this.rangeEnd,
    this.selectedDay,
    this.focusedDay,
    this.rangeSelectionMode = RangeSelectionMode.toggledOn,
    this.taskDeleteModelObj,
  });

  TaskDeleteModel? taskDeleteModelObj;

  DateTime? rangeStart;

  DateTime? rangeEnd;

  DateTime? selectedDay;

  DateTime? focusedDay;

  RangeSelectionMode rangeSelectionMode;

  @override
  List<Object?> get props => [
        rangeStart,
        rangeEnd,
        selectedDay,
        focusedDay,
        rangeSelectionMode,
        taskDeleteModelObj,
      ];
  TaskDeleteState copyWith({
    DateTime? rangeStart,
    DateTime? rangeEnd,
    DateTime? selectedDay,
    DateTime? focusedDay,
    RangeSelectionMode? rangeSelectionMode,
    TaskDeleteModel? taskDeleteModelObj,
  }) {
    return TaskDeleteState(
      rangeStart: rangeStart ?? this.rangeStart,
      rangeEnd: rangeEnd ?? this.rangeEnd,
      selectedDay: selectedDay ?? this.selectedDay,
      focusedDay: focusedDay ?? this.focusedDay,
      rangeSelectionMode: rangeSelectionMode ?? this.rangeSelectionMode,
      taskDeleteModelObj: taskDeleteModelObj ?? this.taskDeleteModelObj,
    );
  }
}
